const text_before = res.map((item) => `${item.words}`)
const res_translated = res.map((item) => `${item.words} translated`)
const responseTranslated = {
  before: text_before,
  after: res_translated
}

document.addEventListener("DOMContentLoaded", (event) => {
  const textNodes = getTextNodes(document.body)
  applyTranslations(textNodes, responseTranslated)
});

function getTextNodes(element) {
  let textNodes = [];
  // Find text nodes
  const treeWalker = document.createTreeWalker(element, NodeFilter.SHOW_TEXT, null, false);
  let node;
  while (node = treeWalker.nextNode()) {
    let words = getWordsFromTextNode(node);
    if (words) {
      textNodes.push({
        element: node,
        words: words,
        type: 1,
        properties: {}
      });
    }
  }
  return textNodes;
}

function applyTranslations(elements, responseTranslated) {
  for (let i = 0; i < elements.length; i += 1) {
    let element = elements[i];
    let contentOriginal = responseTranslated.before[i];
    let contentTranslation = responseTranslated.after[i];
    if (element.words === contentOriginal) {
      element.element.textContent = contentTranslation;
    }
  }
}

function getWordsFromTextNode(textNode) {
  let words = textNode.textContent.trim();
  return words ? words : null;
}